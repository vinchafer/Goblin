/**
 * Render a brand mark. `tone="light"` (default) draws the mark in white — use
 * on a coloured avatar tile. `tone="ink"` draws it in the current ink colour —
 * use on a light surface (integration rows). Unknown ids fall back to a
 * first-letter glyph so the flow never shows a broken image.
 */
export function ProviderLogo({
  id,
  size = 22,
  tone = 'light',
  fallbackLabel,
  className,
}: {
  id: string;
  size?: number;
  tone?: 'light' | 'ink';
  fallbackLabel?: string;
  className?: string;
}) {
  const mark = MARKS[id as BrandId];
  const color = tone === 'light' ? '#fff' : 'currentColor';
  if (!mark) {
    const label = (fallbackLabel ?? id.slice(0, 1)).toUpperCase();
    const style: CSSProperties = {
      width: size, height: size, fontSize: size * 0.5,
      display: 'inline-flex', alignItems: 'center', justifyContent: 'center',
      fontWeight: 700, color,
    };
    return <span style={style} className={className} aria-hidden>{label}</span>;
  }
  return (
    <svg
      width={size}
      height={size}
      viewBox={mark.viewBox}
      fill={color}
      className={className}
      aria-hidden
      style={{ display: 'block' }}
    >
      {mark.body}
    </svg>
  );
}
